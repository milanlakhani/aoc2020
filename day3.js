function gcd(x, y) {
	return y===0?x:gcd(y, x%y);
}

function testTrajectory(right, down, map) {
	const hcf = gcd(right, down);
	const rightstep = right/hcf;
	const downstep = down/hcf;
	let trees=0, rightpos=rightstep, downpos=downstep;
	while (downpos < map.length) {
		if (map[downpos][rightpos] === `#`) {trees++;}
		rightpos = (rightpos+rightstep)%map[0].length;
		downpos += downstep;
	}
	return trees;
}

